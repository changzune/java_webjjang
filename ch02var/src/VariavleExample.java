
public class VariavleExample {
	
	public static void main(String[] args) {
		
		//10을 변수 value의 초기값 (맨처음 넣는 값) 으로 저장 - 주석
		// value 변수의 선언이다.
		int value = 10;
		
		//변수 value 값을 10을 더하는 산술 연산을 수행
		//연산의 결과값을 변수 result의 초기값(맨 처음 넣는 값)으로 저장
		int result = value + 10;
		
		//변수 result 값을 읽고 콘솔에 출력
		System.out.println(result);
		
	}
	

	
}
